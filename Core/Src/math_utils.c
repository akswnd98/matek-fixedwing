#include "math_utils.h"

int max (int a, int b) {
	return a > b ? a : b;
}

int min (int a, int b) {
	return a < b ? a : b;
}

float max_f (float a, float b) {
	return a > b ? a : b;
}

float min_f (float a, float b) {
	return a < b ? a : b;
}
